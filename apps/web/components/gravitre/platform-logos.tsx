// Platform logo card component for display
interface PlatformLogoCardProps {
  name: keyof typeof PlatformLogos
  delay?: number
  className?: string
}

export function PlatformLogoCard({ name, delay = 0, className = "" }: PlatformLogoCardProps) {
  const LogoComponent = PlatformLogos[name]
  
  if (!LogoComponent) return null
  
  return (
    <motion.div
      initial={{ opacity: 0, scale: 0.9 }}
      whileInView={{ opacity: 1, scale: 1 }}
      viewport={{ once: true }}
      transition={{ delay }}
      whileHover={{ scale: 1.05, y: -2 }}
      className={`flex h-20 items-center justify-center rounded-2xl border border-zinc-200 bg-white shadow-sm transition-all hover:shadow-md hover:border-zinc-300 ${className}`}
    >
      <LogoComponent />
    </motion.div>
  )
}

